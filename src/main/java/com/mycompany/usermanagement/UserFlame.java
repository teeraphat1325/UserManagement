    private void clearForm() {
        if(index==-1){
            edtLogin.setText("");
            edtName.setText("");
            edtPassword.setText("");
            cmbRole.setSelectedIndex(1);
            rdoMale.setSelected(true);
            edtLogin.requestFocus();
        }else{
            fillform();
        }
        
    }

